all_files = sorted([f for f in os.listdir(processed_dir) if f.endswith(".wav")])
print(f"处理文件数：{len(all_files)}")

all_results = []

for audio_filename in all_files:
    json_filename = audio_filename.replace(".wav", ".json")
    json_path = os.path.join(alignment_dir, json_filename)

    if not os.path.exists(json_path):
        print(f"✗ 缺少alignment：{audio_filename}")
        continue

    #读取音频
    audio, sr = librosa.load(
        os.path.join(processed_dir, audio_filename),
        sr=SR, mono=True
    )

    #读取音节边界
    with open(json_path, "r", encoding="utf-8") as f:
        segments = json.load(f)

    #计算说话人F0中位数（用于归一化）
    all_f0_values = []
    for seg in segments:
        f0, voiced_flag = extract_f0_pyin(audio, SR, seg["start"], seg["end"])
        if f0 is not None and voiced_flag is not None:
            voiced_f0 = f0[voiced_flag & ~np.isnan(f0)]
            all_f0_values.extend(voiced_f0.tolist())

    if not all_f0_values:
        print(f"✗ 无有效F0：{audio_filename}")
        continue

    speaker_median = np.median(all_f0_values)

    #逐音节提取F0特征
    file_results = []
    for seg in segments:
        char = seg["text"]
        start = seg["start"]
        end   = seg["end"]
        duration_ms = round((end - start) * 1000, 1)

        f0, voiced_flag = extract_f0_pyin(audio, SR, start, end)

        if f0 is None or voiced_flag is None or not np.any(voiced_flag):
            file_results.append({
                "file": audio_filename,
                "char": char,
                "start": start,
                "end": end,
                "duration_ms": duration_ms,
                "speaker_median_hz": round(speaker_median, 2),
                "f0_10pts_semitone": None,
                "C0": None, "C1": None, "C2": None,
                "voiced_ratio": 0.0,
                "error": "no_voiced_frames"
            })
            continue

        #插值
        f0_interp = interpolate_f0(f0, voiced_flag)
        if f0_interp is None:
            file_results.append({
                "file": audio_filename,
                "char": char,
                "start": start,
                "end": end,
                "duration_ms": duration_ms,
                "speaker_median_hz": round(speaker_median, 2),
                "f0_10pts_semitone": None,
                "C0": None, "C1": None, "C2": None,
                "voiced_ratio": 0.0,
                "error": "interp_failed"
            })
            continue

        #说话人归一化
        f0_semitone = hz_to_semitone(f0_interp, speaker_median)

        #时间归一化到10个点
        f0_normalized = normalize_time(f0_semitone, N_POINTS)
        if f0_normalized is None:
            continue

        #DCT系数
        C0, C1, C2 = compute_dct(f0_normalized)

        #voiced比例
        voiced_ratio = float(np.sum(voiced_flag) / len(voiced_flag))

        file_results.append({
            "file": audio_filename,
            "char": char,
            "start": start,
            "end": end,
            "duration_ms": duration_ms,
            "speaker_median_hz": round(speaker_median, 2),
            "f0_10pts_semitone": [round(x, 4) for x in f0_normalized.tolist()],
            "C0": round(float(C0), 4),
            "C1": round(float(C1), 4),
            "C2": round(float(C2), 4),
            "voiced_ratio": round(voiced_ratio, 3),
            "error": None
        })

    #保存每个文件的结果
    out_path = os.path.join(f0_dir, audio_filename.replace(".wav", "_f0.json"))
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(file_results, f, ensure_ascii=False, indent=2)

    all_results.extend(file_results)
    print(f"✓ {audio_filename}  ({len(file_results)}个音节，说话人中位F0={speaker_median:.1f}Hz)")

print(f"\n完成：{len(all_results)} 个音节总计")

