import numpy as np
import pandas as pd
import librosa
import os

df = pd.read_csv(f"{project_root}/f0_clean.csv")

def extract_hnr(audio, sr, start_sec, end_sec):
    """
    用自相关法估计HNR
    HNR(dB) = 10 * log10(R_peak / (1 - R_peak))
    R_peak = 基频处的归一化自相关峰值
    """
    start_sample = int(start_sec * sr)
    end_sample   = int(end_sec * sr)
    segment = audio[start_sample:end_sample]

    if len(segment) < 512:
        return None

    #加窗
    if len(segment) < 2048:
        segment = np.pad(segment, (0, 2048 - len(segment)))

    #归一化自相关
    autocorr = np.correlate(segment, segment, mode='full')
    autocorr = autocorr[len(autocorr)//2:]  #只取正延迟部分
    autocorr = autocorr / (autocorr[0] + 1e-10)  #归一化

    #在基频对应的lag范围内找峰值
    #50Hz → lag=320, 400Hz → lag=40 (at sr=16000)
    min_lag = int(sr / 400)
    max_lag = int(sr / 50)
    max_lag = min(max_lag, len(autocorr) - 1)

    if min_lag >= max_lag:
        return None

    peak = np.max(autocorr[min_lag:max_lag])
    peak = np.clip(peak, 0, 0.9999)

    #转换为dB
    hnr_db = 10 * np.log10(peak / (1 - peak + 1e-10))
    return round(float(hnr_db), 3)

