import librosa
import numpy as np
import json
import os
from scipy.interpolate import interp1d
from scipy.fftpack import dct

SR = 16000
#时间归一化点数
N_POINTS = 10

def extract_f0_pyin(audio, sr, start_sec, end_sec):
    """从音节时间段提取F0轨迹"""
    start_sample = int(start_sec * sr)
    end_sample   = int(end_sec * sr)
    segment = audio[start_sample:end_sample]

    if len(segment) < 512:
        return None, None

    f0, voiced_flag, voiced_probs = librosa.pyin(
        segment,
        fmin=63,
        fmax=400,
        sr=sr,
        frame_length=640,
        hop_length=128
    )
    return f0, voiced_flag

def hz_to_semitone(f0, speaker_median_f0):
    """Hz转半音（说话人归一化）"""
    with np.errstate(divide='ignore', invalid='ignore'):
        semitones = 12 * np.log2(f0 / speaker_median_f0)
    return semitones

def interpolate_f0(f0, voiced_flag):
    """插值填补清音段"""
    if voiced_flag is None or not np.any(voiced_flag):
        return None

    frames = np.arange(len(f0))
    voiced_frames = frames[voiced_flag]
    voiced_f0 = f0[voiced_flag]

    if len(voiced_frames) < 2:
        return None

    interp_func = interp1d(
        voiced_frames, voiced_f0,
        kind='linear',
        bounds_error=False,
        fill_value=(voiced_f0[0], voiced_f0[-1])
    )
    return interp_func(frames)

def normalize_time(f0_interp, n_points=10):
    """时间归一化到n_points个等距点"""
    if f0_interp is None or len(f0_interp) < 2:
        return None
    x_old = np.linspace(0, 1, len(f0_interp))
    x_new = np.linspace(0, 1, n_points)
    interp_func = interp1d(x_old, f0_interp, kind='linear')
    return interp_func(x_new)

def compute_dct(f0_normalized):
    """计算DCT系数"""
    coeffs = dct(f0_normalized, norm='ortho')
    return coeffs[0], coeffs[1], coeffs[2]  # C0, C1, C2

