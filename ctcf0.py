def get_tone(char):
    """获取汉字声调数字 1/2/3/4，轻声返回0"""
    if not char or not re.match(r'[\u4e00-\u9fff]', char):
        return None
    result = lazy_pinyin(char, style=Style.TONE3)
    if not result:
        return None
    pinyin = result[0]
    #提取最后一个数字
    match = re.search(r'[1-4]$', pinyin)
    if match:
        return int(match.group())
    return 0  #轻声

df['tone'] = df['char'].apply(get_tone)

