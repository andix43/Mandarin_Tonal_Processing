def text_to_syllable_phonemes(text):
    """返回：[(汉字, [音素列表]), ...]"""
    text_clean = re.sub(r'[^\u4e00-\u9fff]', '', text)
    result = []
    for char in text_clean:
        pinyin = lazy_pinyin(char, style=Style.TONE3)[0]
        phonemes = list(re.sub(r'[0-9]', '', pinyin))
        result.append((char, phonemes))
    return result

