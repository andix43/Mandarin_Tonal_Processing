def json_to_textgrid_no_gap(json_path, audio_duration, output_path):
    with open(json_path, "r", encoding="utf-8") as f:
        segments = json.load(f)

    n = len(segments)

    # 强制对齐边界：前一个字的xmax = 后一个字的xmin
    boundaries = []
    for i, seg in enumerate(segments):
        if i == 0:
            # 第一个字：xmin从0开始
            xmin = 0.0
        else:
            # 每个字的xmin = 上一个字的xmax
            xmin = boundaries[-1][1]

        # xmax用当前字的end，但最后一个字用音频总时长
        if i == n - 1:
            xmax = round(audio_duration, 4)
        else:
            # 当前字的end和下一个字的start取中点，避免overlap
            current_end = seg["end"]
            next_start = segments[i + 1]["start"]
            if current_end <= next_start:
                # 正常情况：取中点
                xmax = round((current_end + next_start) / 2, 4)
            else:
                # overlap情况：直接用当前end
                xmax = round(current_end, 4)

        boundaries.append((xmin, xmax))

    lines = [
        'File type = "ooTextFile"',
        'Object class = "TextGrid"',
        '',
        'xmin = 0',
        f'xmax = {audio_duration}',
        'tiers? <exists>',
        'size = 1',
        'item []:',
        '    item [1]:',
        '        class = "IntervalTier"',
        '        name = "syllable"',
        '        xmin = 0',
        f'        xmax = {audio_duration}',
        f'        intervals: size = {n}',
    ]

    for i, (seg, (xmin, xmax)) in enumerate(zip(segments, boundaries), 1):
        lines += [
            f'        intervals [{i}]:',
            f'            xmin = {xmin}',
            f'            xmax = {xmax}',
            f'            text = "{seg["text"]}"',
        ]

    with open(output_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))

